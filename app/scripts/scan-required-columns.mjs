#!/usr/bin/env node
/**
 * scan-required-columns.mjs
 *
 * 扫描 src/features/**\*-supabase.ts 的 .from("xxx") + snake_case 字段,
 * 生成 src/lib/required-columns.generated.ts 作为 schema-check 的清单来源。
 *
 * 跑法:
 *   node scripts/scan-required-columns.mjs
 *
 * 集成方式:在 vite 构建前由 npm 脚本自动调用,确保产物始终同步最新代码。
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const FEATURES = join(ROOT, "src/features");
const OUTPUT = join(ROOT, "src/lib/required-columns.generated.ts");

const FALSE_POSITIVES = new Set([
  "from","select","eq","in","limit","order","range","single","insert","update","delete","upsert",
  "or","and","not","is","gte","lte","match","maybe","public","schema","name","role",
  "const","return","type","interface","function","default","true","false","null","undefined",
  "string","number","boolean","void","async","await","new","if","else","switch","case","break",
  "for","while","do","try","catch","finally","throw","this","class","extends","import","export",
  "as","of","let","var","async","on","off",
  // 后端可能误识别的常见词
  "input","output","value","data","item","items","row","rows","record","query","params",
  "table","column","field","fields","key","keys","index","payload","body","status","error","message",
  "set","get","has","is","id","org","patient","user","session","token","date","time","url",
  "supabase","use","session","create","save","load","next","prev","prev_value",
]);

function scanFile(file) {
  const src = readFileSync(file, "utf-8");
  const tables = new Set();
  // 提取 .from("table_name")
  for (const m of src.matchAll(/\.from\(["']([a-z_]+)["']\)/g)) {
    tables.add(m[1]);
  }
  // 提取 snake_case 字段 key
  const keys = new Set();
  for (const m of src.matchAll(/["']([a-z][a-z0-9_]+)["']\s*:/g)) {
    if (!FALSE_POSITIVES.has(m[1])) keys.add(m[1]);
  }
  for (const m of src.matchAll(/\b([a-z][a-z0-9_]+)\s*:\s*input\./g)) {
    if (!FALSE_POSITIVES.has(m[1])) keys.add(m[1]);
  }
  // 提取 .eq("col", ...) .in("col", [...])
  for (const m of src.matchAll(/\.(?:eq|in|gte|lte|match)\(["']([a-z][a-z0-9_]+)["']/g)) {
    if (!FALSE_POSITIVES.has(m[1])) keys.add(m[1]);
  }
  if (tables.size === 0) return [];
  const out = [];
  for (const t of tables) {
    out.push({ table: t, columns: [...keys].sort() });
  }
  return out;
}

function walk(dir, files = []) {
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, e.name);
    if (e.isDirectory()) walk(p, files);
    else if (e.name.endsWith("-supabase.ts")) files.push(p);
  }
  return files;
}

const files = walk(FEATURES);

// 聚合 table -> Set(columns)
const agg = new Map();
for (const f of files) {
  for (const { table, columns } of scanFile(f)) {
    if (!agg.has(table)) agg.set(table, new Set());
    for (const c of columns) agg.get(table).add(c);
  }
}

const lines = [];
lines.push("/**");
lines.push(" * AUTO-GENERATED by scripts/scan-required-columns.mjs — do not edit by hand.");
lines.push(" *");
lines.push(" * 每次构建/代码扫描时重生成,反映前端实际写入的列名。");
lines.push(" * 用于 schema-check.ts 的运行期自检,缺列时给用户提示。");
lines.push(" */");
lines.push("");
lines.push("export const REQUIRED_COLUMNS: ReadonlyArray<{ table: string; column: string }> = [");
const sorted = [...agg.entries()].sort(([a], [b]) => a.localeCompare(b));
for (const [table, cols] of sorted) {
  for (const c of [...cols].sort()) {
    lines.push(`  { table: "${table}", column: "${c}" },`);
  }
}
lines.push("];");
lines.push("");

mkdirSync(dirname(OUTPUT), { recursive: true });
writeFileSync(OUTPUT, lines.join("\n"), "utf-8");
console.log(`[scan-required-columns] wrote ${agg.size} tables / ${[...agg.values()].reduce((s, c) => s + c.size, 0)} columns to ${OUTPUT}`);